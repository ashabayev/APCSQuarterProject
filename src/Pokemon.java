
public class Pokemon {
private String name;
private double weight;
private String id;

public Pokemon()
{
  
}

public void setName(String namer)
{
  name = namer;
}

public void setWeight(double weighty)
{
  weight = weighty;
}

public void setID(String ego)
{
  id = ego;
}

public String getName()
{
  return name;
}
public double getWeight()
{
  return weight;
}
public String id()
{
  return id;
}

}
